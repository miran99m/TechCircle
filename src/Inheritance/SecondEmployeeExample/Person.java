package Inheritance.SecondEmployeeExample;

public class Person {
	// instance variables
	String name;
	int age;
	String phoneNumber;

	// instance method
	public void eat() {
		System.out.println(name + " is eating food");
	}

	public void drink() {
		System.out.println(name + " is drinking water");
	}

	public void sleep() {
		System.out.println(name + " is sleeping");
	}

}
